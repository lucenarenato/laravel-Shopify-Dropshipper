<ul>
<li>    Change the title of the product *</li>
<li>Assign the product to a Collection*</li>
<li>Assign tags to the product (comma separated)*</li>
<li>Assign/Add a product Type for the improted product.*</li>
<li>Enter/change the description of the product.*</li>
<li>Set if the Product is a Prime Product! (This is automatically set by the app however you can change it if you want)</li>
<li>Amazon price: In here the Amazon product price is displayed (not editable)</li>
<li>Set the Price Increase: Set the percentage (%) of the price you want to increase to the product in your store.  </li>
<li>Inventory: Set the quantity of the Inventory you want to show in your store</li>
</ul>
*Optional
Finally press “Save and Add Product” button

